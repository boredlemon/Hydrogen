static nebula_State *globalL = NULL;

static const char *progname = NEBULA_PROGNAME;


#if defined(NEBULA_USE_POSIX)   /* { */

/*
** Use 'sigaction' when available.
*/
static void setsignal (int sig, void (*handler)(int)) {
  struct sigaction sa;
  sa.sa_handler = handler;
  sa.sa_flags = 0;
  sigemptyset(&sa.sa_mask);  /* do not mask any signal */
  sigaction(sig, &sa, NULL);
}

#else           /* }{ */

#define setsignal            signal

#endif                               /* } */


/*
** Hook set by signal function to stop the interpreter.
*/
static void lstop (nebula_State *L, nebula_Debug *ar) {
  (void)ar;  /* unused arg. */
  nebula_sethook(L, NULL, 0, 0);  /* reset hook */
  nebulaL_error(L, "interrupted!");
}


/*
** Function to be called at a C signal. Because a C signal cannot
** just change a Nebula state (as there is no proper synchronization),
** this function only sets a hook that, when called, will stop the
** interpreter.
*/
static void laction (int i) {
  int flag = NEBULA_MASKCALL | NEBULA_MASKRET | NEBULA_MASKLINE | NEBULA_MASKCOUNT;
  setsignal(i, SIG_DFL); /* if another SIGINT happens, terminate process */
  nebula_sethook(globalL, lstop, flag, 1);
}


static void print_usage (const char *badoption) {
  nebula_writestringerror("%s: ", progname);
  if (badoption[1] == 'e' || badoption[1] == 'l')
    nebula_writestringerror("'%s' needs argument\n", badoption);
  else
    nebula_writestringerror("unrecognized option '%s'\n", badoption);
  nebula_writestringerror(
  "usage: %s [options] [script [args]]\n"
  "Available options are:\n"
  "  -e stat   execute string 'stat'\n"
  "  -i        enter interactive mode after executing 'script'\n"
  "  -l mod    require library 'mod' into global 'mod'\n"
  "  -l g=mod  require library 'mod' into global 'g'\n"
  "  -v        show version information\n"
  "  -E        ignore environment variables\n"
  "  -W        turn warnings on\n"
  "  --        stop handling options\n"
  "  -         stop handling options and execute stdin\n"
  ,
  progname);
}


/*
** Prints an error message, adding the program name in front of it
** (if present)
*/
static void l_message (const char *pname, const char *msg) {
  if (pname) nebula_writestringerror("%s: ", pname);
  nebula_writestringerror("%s\n", msg);
}


/*
** Check whether 'status' is not OK and, if so, prints the error
** message on the top of the stack. It assumes that the error object
** is a string, as it was either generated by Nebula or by 'msghandler'.
*/
static int report (nebula_State *L, int status) {
  if (status != NEBULA_OK) {
    const char *msg = nebula_tostring(L, -1);
    l_message(progname, msg);
    nebula_pop(L, 1);  /* remove message */
  }
  return status;
}


/*
** Message handler used to run all chunks
*/
static int msghandler (nebula_State *L) {
  const char *msg = nebula_tostring(L, 1);
  if (msg == NULL) {  /* is error object not a string? */
    if (nebulaL_callmeta(L, 1, "__tostring") &&  /* does it have a metamethod */
        nebula_type(L, -1) == NEBULA_TSTRING)  /* that produces a string? */
      return 1;  /* that is the message */
    else
      msg = nebula_pushfstring(L, "(error object is a %s value)",
                               nebulaL_typename(L, 1));
  }
  nebulaL_traceback(L, L, msg, 1);  /* append a standard traceback */
  return 1;  /* return the traceback */
}


/*
** Interface to 'nebula_pcall', which sets appropriate message function
** and C-signal handler. Used to run all chunks.
*/
static int docall (nebula_State *L, int narg, int nres) {
  int status;
  int base = nebula_gettop(L) - narg;  /* function index */
  nebula_pushcfunction(L, msghandler);  /* push message handler */
  nebula_insert(L, base);  /* put it under function and args */
  globalL = L;  /* to be available to 'laction' */
  setsignal(SIGINT, laction);  /* set C-signal handler */
  status = nebula_pcall(L, narg, nres, base);
  setsignal(SIGINT, SIG_DFL); /* reset C-signal handler */
  nebula_remove(L, base);  /* remove message handler from the stack */
  return status;
}


static void print_version (void) {
  nebula_writestring(NEBULA_COPYRIGHT, strlen(NEBULA_COPYRIGHT));
  nebula_writeline();
}


/*
** Create the 'arg' table, which stores all arguments from the
** command line ('argv'). It should be aligned so that, at index 0,
** it has 'argv[script]', which is the script name. The arguments
** to the script (everything after 'script') Nebula to positive indices;
** other arguments (before the script name) Nebula to negative indices.
** If there is no script name, assume interpreter's name as base.
*/
static void createargtable (nebula_State *L, char **argv, int argc, int script) {
  int i, narg;
  if (script == argc) script = 0;  /* no script name? */
  narg = argc - (script + 1);  /* number of positive indices */
  nebula_createtable(L, narg, script + 1);
  for (i = 0; i < argc; i++) {
    nebula_pushstring(L, argv[i]);
    nebula_rawseti(L, -2, i - script);
  }
  nebula_setglobal(L, "arg");
}


static int dochunk (nebula_State *L, int status) {
  if (status == NEBULA_OK) status = docall(L, 0, 0);
  return report(L, status);
}


static int dofile (nebula_State *L, const char *name) {
  return dochunk(L, nebulaL_loadfile(L, name));
}


static int dostring (nebula_State *L, const char *s, const char *name) {
  return dochunk(L, nebulaL_loadbuffer(L, s, strlen(s), name));
}


/*
** Receives 'globname[=modname]' and runs 'globname = require(modname)'.
*/
static int dolibrary (nebula_State *L, char *globname) {
  int status;
  char *modname = strchr(globname, '=');
  if (modname == NULL)  /* no explicit name? */
    modname = globname;  /* module name is equal to global name */
  else {
    *modname = '\0';  /* global name ends here */
    modname++;  /* module name starts after the '=' */
  }
  nebula_getglobal(L, "require");
  nebula_pushstring(L, modname);
  status = docall(L, 1, 1);  /* call 'require(modname)' */
  if (status == NEBULA_OK)
    nebula_setglobal(L, globname);  /* globname = require(modname) */
  return report(L, status);
}


/*
** Push on the stack the contents of table 'arg' from 1 to #arg
*/
static int pushargs (nebula_State *L) {
  int i, n;
  if (nebula_getglobal(L, "arg") != NEBULA_TTABLE)
    nebulaL_error(L, "'arg' is not a table");
  n = (int)nebulaL_len(L, -1);
  nebulaL_checkstack(L, n + 3, "too many arguments to script");
  for (i = 1; i <= n; i++)
    nebula_rawgeti(L, -i, i);
  nebula_remove(L, -i);  /* remove table from the stack */
  return n;
}


static int handle_script (nebula_State *L, char **argv) {
  int status;
  const char *fname = argv[0];
  if (strcmp(fname, "-") == 0 && strcmp(argv[-1], "--") != 0)
    fname = NULL;  /* stdin */
  status = nebulaL_loadfile(L, fname);
  if (status == NEBULA_OK) {
    int n = pushargs(L);  /* push arguments to script */
    status = docall(L, n, NEBULA_MULTRET);
  }
  return report(L, status);
}


/* bits of various argument indicators in 'args' */
#define has_error	1	/* bad option */
#define has_i		2	/* -i */
#define has_v		4	/* -v */
#define has_e		8	/* -e */
#define has_E		16	/* -E */


/*
** Traverses all arguments from 'argv', returning a mask with those
** needed before running any Nebula code (or an error code if it finds
** any invalid argument). 'first' returns the first not-handled argument
** (either the script name or a bad argument in case of error).
*/
static int collectargs (char **argv, int *first) {
  int args = 0;
  int i;
  for (i = 1; argv[i] != NULL; i++) {
    *first = i;
    if (argv[i][0] != '-')  /* not an option? */
        return args;  /* stop handling options */
    switch (argv[i][1]) {  /* else check option */
      case '-':  /* '--' */
        if (argv[i][2] != '\0')  /* extra characters after '--'? */
          return has_error;  /* invalid option */
        *first = i + 1;
        return args;
      case '\0':  /* '-' */
        return args;  /* script "name" is '-' */
      case 'E':
        if (argv[i][2] != '\0')  /* extra characters? */
          return has_error;  /* invalid option */
        args |= has_E;
        break;
      case 'W':
        if (argv[i][2] != '\0')  /* extra characters? */
          return has_error;  /* invalid option */
        break;
      case 'i':
        args |= has_i;  /* (-i implies -v) *//* FALLTHROUGH */
      case 'v':
        if (argv[i][2] != '\0')  /* extra characters? */
          return has_error;  /* invalid option */
        args |= has_v;
        break;
      case 'e':
        args |= has_e;  /* FALLTHROUGH */
      case 'l':  /* both options need an argument */
        if (argv[i][2] == '\0') {  /* no concatenated argument? */
          i++;  /* try next 'argv' */
          if (argv[i] == NULL || argv[i][0] == '-')
            return has_error;  /* no next argument or it is another option */
        }
        break;
      default:  /* invalid option */
        return has_error;
    }
  }
  *first = i;  /* no script name */
  return args;
}


/*
** Processes options 'e' and 'l', which involve running Nebula code, and
** 'W', which also affects the state.
** Returns 0 if some code raises an error.
*/
static int runargs (nebula_State *L, char **argv, int n) {
  int i;
  for (i = 1; i < n; i++) {
    int option = argv[i][1];
    nebula_assert(argv[i][0] == '-');  /* already checked */
    switch (option) {
      case 'e':  case 'l': {
        int status;
        char *extra = argv[i] + 2;  /* both options need an argument */
        if (*extra == '\0') extra = argv[++i];
        nebula_assert(extra != NULL);
        status = (option == 'e')
                 ? dostring(L, extra, "=(command line)")
                 : dolibrary(L, extra);
        if (status != NEBULA_OK) return 0;
        break;
      }
      case 'W':
        nebula_warning(L, "@on", 0);  /* warnings on */
        break;
    }
  }
  return 1;
}


static int handle_nebulainit (nebula_State *L) {
  const char *name = "=" NEBULA_INITVARVERSION;
  const char *init = getenv(name + 1);
  if (init == NULL) {
    name = "=" NEBULA_INIT_VAR;
    init = getenv(name + 1);  /* try alternative name */
  }
  if (init == NULL) return NEBULA_OK;
  else if (init[0] == '@')
    return dofile(L, init+1);
  else
    return dostring(L, init, name);
}


