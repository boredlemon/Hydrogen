/*
** Main body of stand-alone interpreter (to be called in protected mode).
** Reads the options and handles them all.
*/
static int pmain (nebula_State *L) {
  int argc = (int)nebula_tointeger(L, 1);
  char **argv = (char **)nebula_touserdata(L, 2);
  int script;
  int args = collectargs(argv, &script);
  nebulaL_checkversion(L);  /* check that interpreter has correct version */
  if (argv[0] && argv[0][0]) progname = argv[0];
  if (args == has_error) {  /* bad arg? */
    print_usage(argv[script]);  /* 'script' has index of bad arg. */
    return 0;
  }
  if (args & has_v)  /* option '-v'? */
    print_version();
  if (args & has_E) {  /* option '-E'? */
    nebula_pushboolean(L, 1);  /* signal for libraries to ignore env. vars. */
    nebula_setfield(L, NEBULA_REGISTRYINDEX, "NEBULA_NOENV");
  }
  nebulaL_openlibs(L);  /* open standard libraries */
  createargtable(L, argv, argc, script);  /* create table 'arg' */
  nebula_gc(L, NEBULA_GCGEN, 0, 0);  /* GC in generational mode */
  if (!(args & has_E)) {  /* no option '-E'? */
    if (handle_nebulainit(L) != NEBULA_OK)  /* run NEBULA_INIT */
      return 0;  /* error running NEBULA_INIT */
  }
  if (!runargs(L, argv, script))  /* execute arguments -e and -l */
    return 0;  /* something failed */
  if (script < argc &&  /* execute main script (if there is one) */
      handle_script(L, argv + script) != NEBULA_OK)
    return 0;
  if (args & has_i)  /* -i option? */
    doREPL(L);  /* do read-eval-print loop */
  else if (script == argc && !(args & (has_e | has_v))) {  /* no arguments? */
    if (nebula_stdin_is_tty()) {  /* running in interactive mode? */
      print_version();
      doREPL(L);  /* do read-eval-print loop */
    }
    else dofile(L, NULL);  /* executes stdin as a file */
  }
  nebula_pushboolean(L, 1);  /* signal no errors */
  return 1;
}


int main (int argc, char **argv) {
  int status, result;
  nebula_State *L = nebulaL_newstate();  /* create state */
  if (L == NULL) {
    l_message(argv[0], "cannot create state: not enough memory");
    return EXIT_FAILURE;
  }
  nebula_pushcfunction(L, &pmain);  /* to call 'pmain' in protected mode */
  nebula_pushinteger(L, argc);  /* 1st argument */
  nebula_pushlightuserdata(L, argv); /* 2nd argument */
  status = nebula_pcall(L, 2, 1, 0);  /* do the call */
  result = nebula_toboolean(L, -1);  /* get result */
  report(L, status);
  nebula_close(L);
  return (result && status == NEBULA_OK) ? EXIT_SUCCESS : EXIT_FAILURE;
}