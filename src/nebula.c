/*
** Return the string to be used as a prompt by the interpreter. Leave
** the string (or nil, if using the default value) on the stack, to keep
** it anchored.
*/
static const char *get_prompt (nebula_State *L, int firstline) {
  if (nebula_getglobal(L, firstline ? "_PROMPT" : "_PROMPT2") == NEBULA_TNIL)
    return (firstline ? NEBULA_PROMPT : NEBULA_PROMPT2);  /* use the default */
  else {  /* apply 'tostring' over the value */
    const char *p = nebulaL_tolstring(L, -1, NULL);
    nebula_remove(L, -2);  /* remove original value */
    return p;
  }
}

/* mark in error messages for incomplete statements */
#define EOFMARK		"<eof>"
#define marklen		(sizeof(EOFMARK)/sizeof(char) - 1)


/*
** Check whether 'status' signals a syntax error and the error
** message at the top of the stack ends with the above mark for
** incomplete statements.
*/
static int incomplete (nebula_State *L, int status) {
  if (status == NEBULA_ERRSYNTAX) {
    size_t lmsg;
    const char *msg = nebula_tolstring(L, -1, &lmsg);
    if (lmsg >= marklen && strcmp(msg + lmsg - marklen, EOFMARK) == 0) {
      nebula_pop(L, 1);
      return 1;
    }
  }
  return 0;  /* else... */
}


/*
** Prompt the user, read a line, and push it into the Nebula stack.
*/
static int pushline (nebula_State *L, int firstline) {
  char buffer[NEBULA_MAXINPUT];
  char *b = buffer;
  size_t l;
  const char *prmt = get_prompt(L, firstline);
  int readstatus = nebula_readline(L, b, prmt);
  if (readstatus == 0)
    return 0;  /* no input (prompt will be popped by caller) */
  nebula_pop(L, 1);  /* remove prompt */
  l = strlen(b);
  if (l > 0 && b[l-1] == '\n')  /* line ends with newline? */
    b[--l] = '\0';  /* remove it */
  if (firstline && b[0] == '=')  /* for compatibility with 5.2, ... */
    nebula_pushfstring(L, "return %s", b + 1);  /* change '=' to 'return' */
  else
    nebula_pushlstring(L, b, l);
  nebula_freeline(L, b);
  return 1;
}


/*
** Try to compile line on the stack as 'return <line>;'; on return, stack
** has either compiled chunk or original line (if compilation failed).
*/
static int addreturn (nebula_State *L) {
  const char *line = nebula_tostring(L, -1);  /* original line */
  const char *retline = nebula_pushfstring(L, "return %s;", line);
  int status = nebulaL_loadbuffer(L, retline, strlen(retline), "=stdin");
  if (status == NEBULA_OK) {
    nebula_remove(L, -2);  /* remove modified line */
    if (line[0] != '\0')  /* non empty? */
      nebula_saveline(L, line);  /* keep history */
  }
  else
    nebula_pop(L, 2);  /* pop result from 'nebulaL_loadbuffer' and modified line */
  return status;
}


/*
** Read multiple lines until a complete Nebula statement
*/
static int multiline (nebula_State *L) {
  for (;;) {  /* repeat until gets a complete statement */
    size_t len;
    const char *line = nebula_tolstring(L, 1, &len);  /* get what it has */
    int status = nebulaL_loadbuffer(L, line, len, "=stdin");  /* try it */
    if (!incomplete(L, status) || !pushline(L, 0)) {
      nebula_saveline(L, line);  /* keep history */
      return status;  /* cannot or should not try to add continuation line */
    }
    nebula_pushliteral(L, "\n");  /* add newline... */
    nebula_insert(L, -2);  /* ...between the two lines */
    nebula_concat(L, 3);  /* join them */
  }
}


/*
** Read a line and try to load (compile) it first as an expression (by
** adding "return " in front of it) and second as a statement. Return
** the final status of load/call with the resulting function (if any)
** in the top of the stack.
*/
static int loadline (nebula_State *L) {
  int status;
  nebula_settop(L, 0);
  if (!pushline(L, 1))
    return -1;  /* no input */
  if ((status = addreturn(L)) != NEBULA_OK)  /* 'return ...' did not work? */
    status = multiline(L);  /* try as command, maybe with continuation lines */
  nebula_remove(L, 1);  /* remove line from the stack */
  nebula_assert(nebula_gettop(L) == 1);
  return status;
}


/*
** Prints (calling the Nebula 'print' function) any values on the stack
*/
static void l_print (nebula_State *L) {
  int n = nebula_gettop(L);
  if (n > 0) {  /* any result to be printed? */
    nebulaL_checkstack(L, NEBULA_MINSTACK, "too many results to print");
    nebula_getglobal(L, "print");
    nebula_insert(L, 1);
    if (nebula_pcall(L, n, 0, 0) != NEBULA_OK)
      l_message(progname, nebula_pushfstring(L, "error calling 'print' (%s)",
                                             nebula_tostring(L, -1)));
  }
}


/*
** Do the REPL: repeatedly read (load) a line, evanebulate (call) it, and
** print any results.
*/
static void doREPL (nebula_State *L) {
  int status;
  const char *oldprogname = progname;
  progname = NULL;  /* no 'progname' on errors in interactive mode */
  nebula_initreadline(L);
  while ((status = loadline(L)) != -1) {
    if (status == NEBULA_OK)
      status = docall(L, 0, NEBULA_MULTRET);
    if (status == NEBULA_OK) l_print(L);
    else report(L, status);
  }
  nebula_settop(L, 0);  /* clear stack */
  nebula_writeline();
  progname = oldprogname;
}

